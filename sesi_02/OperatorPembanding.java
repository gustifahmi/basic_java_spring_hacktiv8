public class OperatorPembanding {
    public static void main(String[] args) {
        //Assign nilaiA dan nilaiB
        int nilaiA = 20;
        int nilaiB = 4;

        //Inisiasi hasil
        boolean hasil;

        //Cek nilaiA > nilaiB
        hasil = nilaiA > nilaiB;
        System.out.println(hasil);

        //Cek nilaiA < nilaiB
        hasil = nilaiA < nilaiB;
        System.out.println(hasil);

        //Cek nilaiA >= nilaiB
        hasil = nilaiA >= nilaiB;
        System.out.println(hasil);

        //Cek nilaiA <= nilaiB
        hasil = nilaiA <= nilaiB;
        System.out.println(hasil);

        //Cek nilaiA == nilaiB
        hasil = nilaiA == nilaiB;
        System.out.println(hasil);

        //Cek nilaiA != nilaiB
        hasil = nilaiA != nilaiB;
        System.out.println(hasil);
    }
}
