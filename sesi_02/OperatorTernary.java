public class OperatorTernary {
    public static void main(String[] args) {
        //Inisiasi suka dan jawaban
        boolean suka = true;
        String jawaban;

        //Assign value ke jawaban, iya jika suka = true, tidak jika suka = false
        jawaban = suka ? "iya" : "tidak";

        //Print jawaban
        System.out.println(jawaban);
    }
}
